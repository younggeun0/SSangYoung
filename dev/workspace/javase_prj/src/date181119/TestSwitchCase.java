package date181119;

public class TestSwitchCase {

	public static void main(String[] args) {
		
		int i = 1;
		
		switch(i) {
		
		case 0 : 
			System.out.println("새벽");
			break;
		case 1 :
			System.out.println("아침");
			break;
		case 2 : 
			System.out.println("점심");
			break;
		default :
			System.out.println("저녁");
			break;
		}
	}
}
